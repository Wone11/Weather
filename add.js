
/**
 * Adding two numbers
 */
function Sum(numberOne,numberTwo){
    return numberOne + numberTwo
}
/**
 * Subtraction
 */
function Subtract(one,two){
    return one - two
}

/**
 * Multplying a Number
 */
function Multply(one,two){
    return one * two
}

//call the function and test
console.log('Sum : ' + Sum(100,100))
console.log('Multiply : ' + Multply(100,100))
console.log('Subtract : ' + Subtract(100,60))
